/* What is JouleQueue?
 *
 * It is a thread conscious worker queue with the ablity to have the data processing flow behave in chain gang like configuration. In other words multiple threads may be simultaneously consuming and producing work and this work may be then consumed by other JouleQueues waiting on work to be handed off to them.
*/

